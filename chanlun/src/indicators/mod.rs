pub mod macd;
pub mod kdj;
pub mod rsi;

pub use macd::平滑异同移动平均线;
pub use kdj::随机指标;
pub use rsi::相对强弱指数;

/// K线取值 —— 根据计算方式从K线提取对应的价格
pub fn K线取值(开盘价: f64, 高: f64, 低: f64, 收盘价: f64, 计算方式: &str) -> f64 {
    match 计算方式 {
        "开" => 开盘价,
        "高" => 高,
        "低" => 低,
        "收" => 收盘价,
        "高低均值" => (高 + 低) / 2.0,
        "高低收均值" => (高 + 低 + 收盘价) / 3.0,
        "开高低收均值" => (高 + 低 + 开盘价 + 收盘价) / 4.0,
        _ => 收盘价,
    }
}
